-- ---- Library Tables ----

CREATE TABLE IF NOT EXISTS GENRE (
  ID int NOT NULL AUTO_INCREMENT,
  REFERENCE_NAME varchar(100) DEFAULT NULL,
  PRIMARY KEY (ID)
) ENGINE=MyISAM DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS FORMAT (
  ID int NOT NULL AUTO_INCREMENT,
  REFERENCE_NAME varchar(100) DEFAULT NULL,
  PRIMARY KEY (ID)
) ENGINE=MyISAM DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS LIBRARY_CODE (
  ID int NOT NULL AUTO_INCREMENT,
  PRESENTATION_NAME varchar(255) DEFAULT NULL,
  ALPHABETICAL_NAME varchar(255) DEFAULT NULL,
  CALL_LETTERS varchar(10) DEFAULT NULL,
  CALL_NUMBERS int DEFAULT NULL,
  GENRE_ID int NOT NULL,
  PRIMARY KEY (ID)
) ENGINE=MyISAM DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS LIBRARY_RELEASE (
  ID int NOT NULL AUTO_INCREMENT,
  TITLE varchar(255) DEFAULT NULL,
  LIBRARY_CODE_ID int NOT NULL,
  FORMAT_ID int NOT NULL,
  CALL_NUMBERS int DEFAULT NULL,
  CALL_LETTERS varchar(10) DEFAULT NULL,
  ALTERNATE_ARTIST_NAME varchar(255) DEFAULT NULL,
  TIME_LAST_MODIFIED bigint DEFAULT NULL,
  TIME_CREATED bigint DEFAULT NULL,
  DATE_LOST bigint DEFAULT NULL,
  DATE_FOUND bigint DEFAULT NULL,
  PRIMARY KEY (ID)
) ENGINE=MyISAM DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS LIBRARY_CODE_CROSS_REFERENCE (
  ID int NOT NULL AUTO_INCREMENT,
  SOURCE_LIBRARY_CODE_ID int NOT NULL,
  TARGET_LIBRARY_CODE_ID int NOT NULL,
  NOTE varchar(255) DEFAULT NULL,
  PRIMARY KEY (ID)
) ENGINE=MyISAM DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS RELEASE_CROSS_REFERENCE (
  ID int NOT NULL AUTO_INCREMENT,
  LIBRARY_RELEASE_ID int NOT NULL,
  LIBRARY_CODE_ID int NOT NULL,
  NOTE varchar(255) DEFAULT NULL,
  PRIMARY KEY (ID)
) ENGINE=MyISAM DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS COMPILATION_TRACK_ARTIST (
  LIBRARY_RELEASE_ID int NOT NULL,
  ARTIST_NAME varchar(255) NOT NULL,
  TRACK_TITLE varchar(255) DEFAULT NULL,
  TRACK_POSITION varchar(20) DEFAULT NULL,
  INDEX idx_cta_release (LIBRARY_RELEASE_ID),
  INDEX idx_cta_artist (ARTIST_NAME)
) ENGINE=MyISAM DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

-- ---- Flowsheet Tables ----

CREATE TABLE IF NOT EXISTS FLOWSHEET_RADIO_SHOW_PROD (
  ID int NOT NULL AUTO_INCREMENT,
  STARTING_RADIO_HOUR bigint NOT NULL DEFAULT 0,
  DJ_NAME varchar(255) DEFAULT NULL,
  DJ_ID int DEFAULT NULL,
  DJ_HANDLE varchar(255) DEFAULT NULL,
  SHOW_NAME varchar(255) DEFAULT NULL,
  SPECIALTY_SHOW_ID int DEFAULT NULL,
  WORKING_HOUR bigint NOT NULL DEFAULT 0,
  SIGNON_TIME bigint DEFAULT NULL,
  SIGNOFF_TIME bigint DEFAULT NULL,
  TIME_LAST_MODIFIED bigint DEFAULT NULL,
  TIME_CREATED bigint DEFAULT NULL,
  MODLOCK int DEFAULT NULL,
  SHOW_ID int DEFAULT NULL,
  PRIMARY KEY (ID)
) ENGINE=MyISAM DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

CREATE TABLE IF NOT EXISTS FLOWSHEET_ENTRY_PROD (
  ID int NOT NULL AUTO_INCREMENT,
  ARTIST_NAME varchar(255) DEFAULT NULL,
  ARTIST_ID int DEFAULT NULL,
  SONG_TITLE varchar(255) DEFAULT NULL,
  RELEASE_TITLE varchar(255) DEFAULT NULL,
  RELEASE_FORMAT_ID int DEFAULT NULL,
  LIBRARY_RELEASE_ID int DEFAULT NULL,
  ROTATION_RELEASE_ID int DEFAULT NULL,
  LABEL_NAME varchar(255) DEFAULT NULL,
  RADIO_HOUR bigint DEFAULT NULL,
  START_TIME bigint DEFAULT NULL,
  STOP_TIME bigint DEFAULT NULL,
  RADIO_SHOW_ID int DEFAULT NULL,
  SEQUENCE_WITHIN_SHOW int DEFAULT NULL,
  NOW_PLAYING_FLAG int DEFAULT NULL,
  FLOWSHEET_ENTRY_TYPE_CODE_ID int DEFAULT NULL,
  TIME_LAST_MODIFIED bigint DEFAULT NULL,
  TIME_CREATED bigint DEFAULT NULL,
  REQUEST_FLAG int DEFAULT NULL,
  GLOBAL_ORDER_ID bigint DEFAULT NULL,
  BMI_COMPOSER varchar(255) DEFAULT NULL,
  SEGUE_FLAG int NOT NULL DEFAULT 0,
  PRIMARY KEY (ID)
) ENGINE=MyISAM DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

